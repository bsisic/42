#include "libft.h"

void	*ft_memmove(void *s1, const void *s2, size_t n)
{
	size_t	i;

	i = n - 1;
	if (s2 <= s1)
	{
		while (n--)
		{
			((char *)s1)[i] = ((char *)s2)[i];
			i--;
		}
	}
	else
		ft_memcpy(s1, s2, n);
	return (s1);
}
