#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*s;

	s = (char *)ft_memalloc(sizeof(char) * (size + 1));
	if (s)
	{
		while (size)
			s[size--] = '\0';
		*s = '\0';
	}
	return (s);
}
